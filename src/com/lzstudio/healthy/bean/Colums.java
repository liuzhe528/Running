package com.lzstudio.healthy.bean;

public class Colums {
	/**
	 * 栏目id
	 */
	private int id;
	/**
	 * 栏目名字
	 */
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
